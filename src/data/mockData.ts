export function generateMockAnswers(): Answer[] {
  return allQuestions.map((q) => ({
    questionId: q.id,
    section: q.section,
    type: q.type,
    value: MOCK_ANSWERS[q.id] || (q.type === 'ranking' ? [] : q.type === 'open-ended' ? '[Mock answer]' : 'UNSPECIFIED'),
    timestamp: Date.now(),
  }))
}

